def sep():
    print('\n')

# Escape Characters


spam = 'Say hi to Bob\'s mother.'
print(spam)

sep()

spamFirst = 'Say hi to Bob\t mother'
print(spamFirst)

sep()

spamSecond = 'Say hi to Bob\n mother'
print(spamSecond)
sep()


spamThird = 'Say hi to Bob\\ mother'
print(spamThird)

sep()

print("Hello there!\nHow are are you?\nI\'m doing fine.")
