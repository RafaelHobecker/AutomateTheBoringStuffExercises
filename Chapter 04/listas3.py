
# Augmented Assignment Operators

spam = 42
spam = spam + 1
print(spam)

print('\n')
# As a shortcut, you can use the augmented assignment operator += to do the same thing:

spam = 42
spam += 1
print(spam)

print('\n')

#The += operator can also do string and list concatenation
spam = 'Hello'
spam += ' World!'
print(spam)

print('\n')

# The *= operator can do string and list replication

bacon = ['Zophie']
bacon *= 3
print(bacon)

