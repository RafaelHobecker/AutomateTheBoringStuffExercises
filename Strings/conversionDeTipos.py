# Conversion de Tipos

# A menudo, los numeros se leen inicialmente como Strings(por ejemplo, desde una entrada de usuario o un archivo) y deben convertirse para realizar calculos.

#1. str() Convierte a String(texto).
num = str(123)
print(num)
#Imprime "123"
print(type(num))


#2. int() Convierte a Integer(entero).
integer = int("345")
print(integer)
#Imprime 345
print(type(integer))


#3. float() Convierte a Float(decimal).
decimal = float("1.99")
print(decimal)

#Imprime 1.99
print(type(decimal))
